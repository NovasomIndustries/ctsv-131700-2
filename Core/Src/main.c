void TIM1_IRQ_Callback(void)
{
	ramp_tim1++;
	if ( ramp_tim1 > RAMP_STEP )
	{
		ramp_tim1 = 0;
		tim1_value += STEP;
		if ( tim1_value < LIMIT )
			htim1.Instance->CCR1 = tim1_value;
	}
}


void TIM14_IRQ_Callback(void)
{
	poll_counter--;
	if ( poll_counter == 0 )
	{
		poll_counter = TIM14_COUNTS;
		ms10_flag = 1;
	}
}

void print_set_speed(void)
{
	sprintf(lcd_wr_stct.line, "Set: %d\" ", time_set);
	lcd_wr_stct.xpos = 10;
	lcd_wr_stct.ypos = (uint8_t)(ST7735_HEIGHT-CHAR_H)/2;
	if ( time_set < 10 )
	{
		lcd_wr_stct.fore_color = ST7735_RED;
	}
	LcdWrite16x26(&lcd_wr_stct);
}

void print_work_end(void)
{
	sprintf(lcd_wr_stct.line, "Finished !!" );
	lcd_wr_stct.xpos = 10;
	lcd_wr_stct.ypos = (uint8_t)(ST7735_HEIGHT-CHAR_H)/2;
	lcd_wr_stct.fore_color = ST7735_GREEN;
	LcdWrite16x26(&lcd_wr_stct);
	lcd_wr_stct.fore_color = ST7735_WHITE;
}

void TIM17_IRQ_Callback(void)
{
	down_counter--;
	if ( down_counter == 0 )
	{
		down_counter = TIM17_COUNTS;
		time_set--;
		if ( time_set == 0 )
		{
			HAL_TIM_Base_Stop_IT(&htim17);
			time_set = stored_time_set;
			status = STOPPED;
			tim1_value = STEP;
			htim1.Instance->CCR1 = tim1_value;
			HAL_TIM_PWM_Stop_IT(&htim1,TIM_CHANNEL_1);
			finished_flag = 1;
			finished_time = FINISHED_TIME_BANNER;
		}
		else
			print_set_speed();
		}
}

